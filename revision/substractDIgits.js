// Subtract Digits Ended
// Description

// You are given an integer n.

// On each step, you may subtract one of the digits from the number.
// Find min steps that are required to make the number equal to 0.

// Input
// Input Format

// The first line contains the number of testcases, T

// For each testcase:

// The first and only contains the number N
// Constraints

// 1<=T<=5

// 1<=N<=50

// Output
// For each testcase, print the minimum number of steps.

// Sample Input 1

// 4
// 20
// 19
// 15
// 1
// Sample Output 1

// 4
// 3
// 3
// 1
// Hint

// 20-> 18 -> 10 -> 9 ->0

// 19 -> 10 -> 9 -> 0

// 15 -> 10 -> 9 ->0

// 1 ->0
