// Equal Thirds - Arrays Ended
// Description

// You are given an array A of length N where N is divisible by 3. Determine whether it is possible to divide the array into three contiguous subarrays of equal length(N/3) such that the sum of the elements in each subarray is the same.

// Input
// First line contains T, no of testcases

// Second Line contains integer N, representing size of string.

// Third-line contains N integers.

// Constraints

// 1 <= T <= 10

// 1 <= N <= 1000

// N % 3 = 0

// Output
// Output the required answer

// Sample Input 1

// 1
// 9
// 3 4 5 2 1 9 3 6 3
// Sample Output 1

// YES
// Hint

// Explanation:
// The array can be divided into three subarrays of equal length and their sums are [3, 4, 5], [2, 1, 9], and [3, 6, 3], which all have a sum of 12. Therefore, the answer is YES.
