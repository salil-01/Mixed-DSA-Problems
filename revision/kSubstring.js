// K - Substring Ended
// Description

// Given a string, find the length of the longest substring of a string containing k distinct characters. If there is no possible substring then print -1.

// Input
// Input Format

// First-line contains T, no of test cases.

// First-line of each test case contains N, the length of the string, and an integer k.

// Second-line of each test case contains string s of length N.

// Constraints

// 1 <= T <= 10

// 1 <= N <= 10^5

// 1 <= k <= 10^5

// Output
// For each test case print the length of the longest substring and if there is no such substring print -1.

// Sample Input 1

// 2
// 11 3
// aabacbebebe
// 4 2
// aaaa
// Sample Output 1

// 7
// -1
// Hint

// For the first test case, "cbebebe" is the longest substring with 3 distinct characters, so the answer is 7.

// For the second test case, there is no substring present that has 2 distinct characters, so the answer is -1.
